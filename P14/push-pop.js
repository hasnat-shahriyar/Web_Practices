// Add or remove Element from array using push, pop
var numbers = [60, 50, 55, 50, 45];
// Push or Add
numbers.push(40)
console.log(numbers);

var friends = ['Emon', 'Masud', 'Shad', 'Mehedi'];
friends.push('Rakib');
console.log(friends);

// Use Pop to get last Element
numbers.pop();
friends.pop();
friends.pop();
console.log(numbers);
console.log(friends);